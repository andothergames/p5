var sketch = function (p) {
  p.setup = function () {
    p.createCanvas(400, 400);
  };

  p.draw = function () {
    p.noStroke();
    p.background("#f0c7ee");

    //eyes

    p.fill("#000000");
    p.ellipse(150, 180, 60);
    p.ellipse(250, 180, 60);
  };

  function drawCircle(c, x, y, r) {
    p.fill(c);
    ellipse(x, y, r);
  }
};

